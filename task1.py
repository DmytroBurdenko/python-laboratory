import math
print("Бурденко Дмитро Сергійович \n Лабораторна робота №1 \n Варіант 3 \n " 
      "Завдання №1. Розрахунок гіпотенузи, площі та периметра трикутника. ")
a = float(input("Довжина першого катета: "))
b = float(input("Довжина другого катета: "))
hypot = math.sqrt(a**2 + b**2)
round(hypot, 2)
perimeter = (a+b+hypot)
area = (a*b/2)
print("Гіпотенуза трикутника: ", hypot, "; \n",
      "Периметр трикутника: ", perimeter, "; \n",
      "Площа трикутника: ", area, ".")
